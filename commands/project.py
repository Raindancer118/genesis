def create_project(name=None, template_key=None, use_git=None, auto_confirm=False):
    """Create a new project, optionally using provided parameters instead of prompts."""
    console.print(Panel.fit("[bold cyan]üöÄ Genesis Project Builder[/bold cyan]", border_style="cyan"))

    project_name = name
    if project_name:
        if Path(project_name).exists():
            console.print(f"[red]A project or directory named '{project_name}' already exists.[/red]")
            return
    else:
        project_name = questionary.text(
            "What is the name of your project?",
            validate=lambda text: True if len(text) > 0 and not Path(text).exists()
            else "Name cannot be empty or already exist."
        ).ask()
        if not project_name:
            console.print("[red]Cancelled.[/red]")
            return

    template_choice = template_key
    if template_choice:
        if template_choice not in TEMPLATES:
            console.print(
                f"[red]Unknown template '{template_choice}'. Available options: {', '.join(TEMPLATES)}[/red]"
            )
            return
    else:
        template_choice = questionary.select(
            "Choose a project template:",
            choices=get_template_choices()
        ).ask()
        if not template_choice:
            console.print("[red]Cancelled.[/red]")
            return

    if use_git is None:
        use_git = questionary.confirm("Initialize a Git repository?", default=True).ask()
        if use_git is None:
            console.print("[red]Cancelled.[/red]")
            return

    summary = Text.assemble(
        ("Project Name: ", "bold"), (f"{project_name}\n"),
        ("Template:     ", "bold"), (f"{template_choice}\n"),
        ("Use Git:      ", "bold"), ("Yes" if use_git else "No")
    )
    console.print(Panel(summary, title="[yellow]Summary[/yellow]", expand=False))

    proceed = auto_confirm or questionary.confirm("Proceed with creation?").ask()
    if not proceed:
        console.print("[red]Cancelled.[/red]")
        return

    console.print("\n[bold]Building project...[/bold]")
    project_path = Path(project_name)
    try:
        project_path.mkdir()
    except FileExistsError:
        console.print(f"[red]A directory named '{project_name}' already exists.[/red]")
        return

    scaffold_function = TEMPLATES[template_choice]
    if scaffold_function:
        scaffold_function(project_path, use_git)
    else:
        console.print(f"  ‚úÖ [green]Created empty directory '{project_name}'[/green]")

    console.print(f"\n[bold green]üéâ Project '{project_name}' created successfully![/bold green]")


def build_from_template(project_name, template_string):
    """Build a project structure from an indented text template."""
    if not template_string:
        console.print("[red]No template provided. Nothing to build.[/red]")
        return

    project_path = Path(project_name)
    try:
        project_path.mkdir()
    except FileExistsError:
        console.print(f"[red]A directory named '{project_name}' already exists.[/red]")
        return

    stack = [project_path]
    for line_number, raw_line in enumerate(template_string.splitlines(), start=1):
        stripped = raw_line.strip()
        if not stripped or stripped.startswith('#'):
            continue

        indent = len(raw_line) - len(raw_line.lstrip(' '))
        if indent % 4 != 0:
            console.print(
                f"[red]Invalid indentation on line {line_number}: '{raw_line.strip()}'. Use multiples of four spaces.[/red]"
            )
            continue

        depth = indent // 4
        if depth >= len(stack):
            console.print(
                f"[red]Line {line_number} is indented too deeply without a parent directory: '{raw_line.strip()}'[/red]"
            )
            continue

        while len(stack) > depth + 1:
            stack.pop()

        parent = stack[depth]
        if stripped.endswith('/'):
            directory_name = stripped.rstrip('/').strip()
            target = parent / directory_name
            target.mkdir(exist_ok=True)
            stack.append(target)
        else:
            file_path = parent / stripped
            file_path.parent.mkdir(parents=True, exist_ok=True)
            if not file_path.exists():
                file_path.touch()

    console.print(f"\n[bold green]üìÅ Built template structure in '{project_name}'.[/bold green]")


def build_from_json(project_name, json_structure, use_git=False):
    """Build a project structure from a JSON/dict representation.
    
    The JSON structure format:
    {
        "project-name": {
            "directory": {
                "subdirectory": {},
                "file.txt": "file content here",
                "another-file.txt": null
            }
        }
    }
    
    - Directories are represented as dict keys with dict values (can be empty {})
    - Files are represented as dict keys with string values (content) or null (empty file)
    """
    if isinstance(json_structure, str):
        try:
            json_structure = json.loads(json_structure)
        except json.JSONDecodeError as e:
            console.print(f"[red]Invalid JSON: {e}[/red]")
            return
    
    if not isinstance(json_structure, dict):
        console.print("[red]JSON structure must be an object/dict[/red]")
        return
    
    # If the root has a single key that matches the project name, use that as root
    if len(json_structure) == 1:
        root_key = list(json_structure.keys())[0]
        # Use the content of that key as the actual structure
        structure = json_structure[root_key]
    else:
        structure = json_structure
    
    project_path = Path(project_name)
    try:
        project_path.mkdir()
    except FileExistsError:
        console.print(f"[red]A directory named '{project_name}' already exists.[/red]")
        return
    
    def create_structure(parent_path: Path, structure_dict: dict):
        """Recursively create directory structure from dict."""
        if not isinstance(structure_dict, dict):
            return
            
        for name, content in structure_dict.items():
            target_path = parent_path / name
            
            if isinstance(content, dict):
                # It's a directory
                target_path.mkdir(exist_ok=True)
                create_structure(target_path, content)
            elif content is None:
                # It's an empty file
                target_path.parent.mkdir(parents=True, exist_ok=True)
                target_path.touch()
            elif isinstance(content, str):
                # It's a file with content
                target_path.parent.mkdir(parents=True, exist_ok=True)
                target_path.write_text(content)
            else:
                console.print(f"[yellow]Warning: Unknown type for '{name}', treating as empty file[/yellow]")
                target_path.parent.mkdir(parents=True, exist_ok=True)
                target_path.touch()
    
    create_structure(project_path, structure)
    
    if use_git:
        _init_git_repo(project_path, use_git)
    
    console.print(f"\n[bold green]üìÅ Built project structure in '{project_name}'.[/bold green]")
